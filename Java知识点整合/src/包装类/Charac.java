package 包装类;

public class Charac {
    public static void main(String[] args) {
        String str= Integer.toString(80);
        String str2 = Integer.toBinaryString(80);
        String str3 = Integer.toHexString(80);
        String str4 = Integer.toOctalString(80);
        System.out.println(str2);
        System.out.println(str3);
        System.out.println(str4);
    }
}
